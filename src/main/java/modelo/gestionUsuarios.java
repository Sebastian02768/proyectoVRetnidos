    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        datos_Usuario vista = new datos_Usuario();

        // Crear un panel contenedor y a√±adir el contenido de eliminarUsuario
        JPanel panelContenedor = new JPanel(new BorderLayout());
        panelContenedor.add(vista.getContentPane(), BorderLayout.CENTER);

        // Ajustar el tama√±o del panel contenedor
        panelContenedor.setSize(vista.getContentPane().getPreferredSize());
        panelContenedor.setVisible(true);

        // Limpiar el desktopPane y a√±adir el panel contenedor
        desktopPane.removeAll();
        desktopPane.add(panelContenedor);

        // Calcular la posici√≥n para centrar el panel
        int x = (desktopPane.getWidth() - panelContenedor.getWidth()) / 2;
        int y = (desktopPane.getHeight() - panelContenedor.getHeight()) / 2;
        panelContenedor.setLocation(x, y);

        desktopPane.revalidate();
        desktopPane.repaint();
    }//GEN-LAST:event_btnIngresarActionPerformed

