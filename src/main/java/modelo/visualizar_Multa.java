public void visualizar() {
    Multa mul = new Multa(); // Crear una instancia del objeto usuario
    DefaultTableModel model = new DefaultTableModel(); // Crear un nuevo modelo de tabla
    model.setColumnIdentifiers(new Object[]{"Id", "Placa", "Tipo Multa", "Fecha"});
    model.setRowCount(0); // Limpiar el modelo antes de agregar nuevos datos

    try {
        ResultSet rs = mul.visualizarMulta(); // Llamar para obtener el ResultSet

        // Recorrer el ResultSet y agregar filas al modelo
        while (rs.next()) {
            int id = rs.getInt("id");
            String placa = rs.getString("placaFK");
            String tipoMulta = rs.getString("tipo_multa_idFK");
            String fecha = rs.getString("fecha_infraccion");

            // Agregar una nueva fila al modelo con los datos obtenidos
            model.addRow(new Object[]{id, placa,tipoMulta, fecha});
        }

        // Asignar el modelo a la tabla (aquí deberías tener una tabla llamada tablaUsuarios)
        tablaMultas.setModel(model);

    } catch (SQLException e) {
        e.printStackTrace();
        // Manejo de excepciones
    }
}






